def calculate_level(network_link):
    """
    Вычисляет уровень (глубину) звена в цепочке поставщиков.

    Уровень определяется как количество промежуточных поставщиков до вершины цепочки.
    Например:
        - У верхнего звена (у которого нет поставщика) уровень 0.
        - У звена, которое напрямую связано с верхним звеном — уровень 1.
        - И так далее.

    :param network_link: Экземпляр модели NetworkLink, для которого вычисляется уровень.
    :return: Целое число, представляющее уровень звена.
    """
    level = 0
    current = network_link.supplier  # Начинаем с поставщика текущего звена

    while current:
        level += 1
        current = current.supplier  # Переходим к следующему поставщику выше по цепочке

    return level
